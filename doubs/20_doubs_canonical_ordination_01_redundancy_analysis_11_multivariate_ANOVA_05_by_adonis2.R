# Permutational MANOVA using adonis2()
# by = "term":  default, asks the function to compute the sums of squares and assess the significance of the terms
# in the order of their mention in the code

# In balanced designs this is not important: reversing the order of the two factors will produce the exact same result.
# In the case of unbalanced designs, however, changing the order changes the results because of the non-orthogonality of the factors,
# which in fact introduces a common fraction.



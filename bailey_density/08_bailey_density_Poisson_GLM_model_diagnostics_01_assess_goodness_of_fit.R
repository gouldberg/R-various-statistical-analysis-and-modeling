# ------------------------------------------------------------------------------
# data:  Bailey Density
# ------------------------------------------------------------------------------

DF1 <- read.table(file = "BaileyDensity.txt", header = TRUE)


str(DF1)


dim(DF1)


car::some(DF1)



# ----------
# missing values for the spatial coordinates to be removed
colSums(is.na(DF1))


DF  <- na.exclude(DF1)



# ----------
M1 <- glm(TotAbund ~ MeanDepth, data = DF, family = poisson(link = "log"))

M2 <- glm(TotAbund ~ MeanDepth * fPeriod,  data = DF,  family = poisson)

DF$LogSA <- log(DF$SweptArea)

M3 <- glm(TotAbund ~ MeanDepth * factor(Period) + offset(LogSA), data = DF,  family = poisson)



# ------------------------------------------------------------------------------
# Goodness of Fit:  The proportion of deviance explained
# ------------------------------------------------------------------------------

# proportion of deviance explained by this model
1 - M1$deviance / M1$null.deviance

1 - M2$deviance / M2$null.deviance

1 - M3$deviance / M3$null.deviance



# -->
# The proportion of deviance explained of M1 and M2 is less than 0.5
# but for M3, the proporation is over 0.67


